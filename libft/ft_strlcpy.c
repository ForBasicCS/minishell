#include "libft.h"

size_t	ft_strlcpy(char *dest, char const *src, size_t size)
{
	size_t	i;
	size_t	len;

	len = ft_strlen(src);
	i = 0;
	if (size == 0)
		return (len);
	while (*src && i++ < size - 1)
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = 0;
	return (len);
}
